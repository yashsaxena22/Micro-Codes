#start=stepper_motor.exe#

DATA SEGMENT
    CWR EQU 0C6H
    PORTA EQU 0C0H
    DATA ENDS
CODE SEGMENT
ASSUME CS: CODE DS:DATA 
START:
MOV AL, 80H
OUT CWR, AL 
MOV AL, 88H
MOV CX, 2000     
AGAIN1:
OUT PORTA, AL 
ROL AL, 01
DEC CX
JNZ AGAIN1
MOV AH, 88H
MOV CX, 2000   
AGAIN2:
OUT PORTA, AL
 
ROR AL, 01
DEC CX
JNZ AGAIN2
MOV AH, 4CH
INT 21H
CODE ENDS  
END START    




 
