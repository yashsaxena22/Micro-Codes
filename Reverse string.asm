DATA SEGMENT
STRING DB 'my country is India', '$'
DATA ENDS
CODE SEGMENT
      ASSUME CS: CODE  DS: DATA    
START:
MOV AX,DATA
MOV DS,AX
MOV SI, OFFSET STRING
MOV CX, 0H
LOOP1:
MOV AX, [SI]
CMP AL, '$'
JE L1
PUSH [SI]
INC SI
INC CX
JMP LOOP1
L1:
MOV SI, OFFSET STRING
LOOP2:
CMP CX,0
JE L2
POP DX
XOR DH, DH
MOV [SI], DX
INC SI
DEC CX
JMP LOOP2
L2:
MOV [SI],'$ '
LEA DX,STRING
MOV AH, 09H
INT 21H
MOV AH, 4CH
INT 21H
END START
CODE ENDS
